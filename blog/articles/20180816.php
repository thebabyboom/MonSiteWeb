<p>
  De mon coté va vraiment falloir que je me calme en sortie, parce que
l'euphorie des sorties quand elle retombe elle fait mal.
je crois que c'est pour ça que mon cerveau travaille sans arret en ce moment.
je revis des moments que je n'avais plus vécu depuis très longtemps,
ça me rend nostalgique et ça me déprime quand ça s'arrete ;
parce que à nouveau je me retrouve seul, avec pour unique compagnon mes pensées.
Des pensées qui amènent des questions, des questions qui n'ont pas de réponses
parce que soit je n'ose pas les poser, soit je n'ai pas la possibilité de les poser
du coup ça me force à vouloir deux choses, soit que les weekend ne s’arrêtent jamais
soit que tout s’arrête à jamais
</p>
